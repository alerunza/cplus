#include <iostream>

using namespace std;

float somma(float x, float y){

    float somma;

    somma = x + y;

    cout<<somma<<endl;

    return somma;
}

float differenza(float x, float y){

    float diff;

    diff = x - y;

    cout<<diff<<endl;

}

float prodotto(float x, float y){

    float prod;

    prod = x * y;

    cout<<prod<<endl;

}